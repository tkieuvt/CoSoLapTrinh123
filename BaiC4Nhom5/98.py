def hex2int(x):
    if x.upper() == "A" : return 10
    if x.upper() == "B" : return 11
    if x.upper() == "C" : return 12
    if x.upper() == "D" : return 13
    if x.upper() == "E" : return 14
    if x.upper() == "F" : return 15
